// Function to get Hinglish translation
export const getHinglishText = (englishText: string): string => {
  return hinglishTranslations[englishText] || englishText;
};

// Function to get bilingual text (English with Hinglish in parentheses)
export const getBilingualText = (englishText: string): string => {
  const hinglishText = getHinglishText(englishText);
  return hinglishText !== englishText ? `${englishText} (${hinglishText})` : englishText;
};

// Function to translate entire content objects
export const translateContent = (content: any): any => {
  if (typeof content === 'string') {
    return getHinglishText(content);
  } else if (Array.isArray(content)) {
    return content.map(item => translateContent(item));
  } else if (typeof content === 'object' && content !== null) {
    const translated: any = {};
    for (const key in content) {
      translated[key] = translateContent(content[key]);
    }
    return translated;
  }
  return content;
};